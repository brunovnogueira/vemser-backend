SELECT LOGRADOURO, CEP 
  FROM ENDERECO 
  WHERE UPPER(LOGRADOURO) 
  LIKE 'A%';
 
SELECT * 
  FROM ENDERECO 
  WHERE CEP 
  LIKE '%0';
 
SELECT * 
  FROM ENDERECO 
  WHERE NUMERO 
  BETWEEN 1 AND 100;
 
SELECT * 
  FROM ENDERECO 
  WHERE UPPER(LOGRADOURO) 
  LIKE 'RUA%' 
  ORDER BY CEP DESC;

SELECT COUNT(LOGRADOURO) 
  FROM ENDERECO;
 
SELECT ID_CIDADE,COUNT(LOGRADOURO) 
  FROM ENDERECO 
  GROUP BY ID_CIDADE;
